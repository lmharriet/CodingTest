#include <iostream>
#include <string>
#include <vector>

using namespace std;
/*
내적
길이가 같은 두 1차원 정수 배열 a,b가 매개변수로 주어집니다.
a와b의 내적을 return 하도록 solution 함수를 완성해주세요.

이때,a와 b의 내적은 a[0]*b[0] +a[1]*b[1] + ......+ a[n-1]*b[n-1] 입니다.(n은 a,b의 길이)

제한사항
a,b의 길이는 1이상 1,000이하입니다.
a,b의 모든 수는 -1000이상 1000이하입니다.

*/

int innerProduct(vector<int> a, vector<int> b) {
    int answer = 0;

    for (int i = 0; i < a.size(); i++)
    {
        answer += a[i] * b[i];
    }

    return answer;
}
