#include <string>
#include <vector>
#include <iostream>
using namespace std;
/*
짝수 홀수
정수 num이 짝수일 경우 Even을 반환하고 홀수인 경우 Odd를 반환하는 함수, solution을 완성해주세요.

제한 조건
num은 int 범위의 정수입니다.
은 짝수입니다.

*/
string solution(int num) {
    string even = "Even";
    string odd = "Odd";
    if (num % 2 == 0) return even;
    else return odd;
}